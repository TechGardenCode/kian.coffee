#!/usr/bin/env node
// Reads the homelab Ansible inventory and emits a typed lab.generated.ts
// so the /lab page stays accurate as infra evolves. Runs at build time.
// SAFETY: reads only hosts.yml — never touches vault files or host_vars/*
// that may contain tokens. Only public-safe fields (hostname, role) are
// emitted — IP addresses are deliberately dropped to keep the public lab
// page free of internal network topology.

import { existsSync, readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import yaml from 'js-yaml';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..', '..', '..', '..');
const inventoryPath = resolve(repoRoot, 'ansible', 'inventory', 'hosts.yml');
const outPath = resolve(__dirname, '..', 'src', 'app', 'content', 'lab.generated.ts');

// This script runs in two contexts:
//   1. Local dev — the submodule is checked out inside the parent
//      `kian.sh` monorepo, so `../../../../ansible/inventory/hosts.yml`
//      exists and we regenerate from source.
//   2. CI / Docker — only the submodule is checked out. The inventory
//      file isn't reachable. Fall back to the committed generated file.
if (!existsSync(inventoryPath)) {
  console.log(
    `lab.generated.ts ← inventory not reachable at ${inventoryPath}; ` +
      `using committed artifact at ${outPath}`
  );
  process.exit(0);
}

const ROLE_LABELS = {
  proxmox: 'Proxmox host',
  raspbian: 'Raspberry Pi',
  nas: 'NAS',
  lgtm: 'Observability VM',
  arr: 'Media VM',
  wings: 'Game server node',
  dns: 'DNS node',
};

const GROUP_DESCRIPTIONS = {
  proxmox: 'Hypervisor hosts running most VM-backed clusters.',
  raspbian: 'Low-power utility nodes — DNS primary and edge load balancer.',
  nas: 'ZFS storage with Cloud Sync + RSync for 3-2-1 backup.',
  lgtm: 'Self-hosted Loki + Grafana + Tempo + Mimir stack.',
  arr: 'Media library automation — *arr suite on a dedicated VM.',
  wings: 'Pelican game-server control plane on the DMZ VLAN.',
  dns: 'Technitium DNS nodes — anycast-style via keepalived.',
};

function loadInventory() {
  const raw = readFileSync(inventoryPath, 'utf8');
  return yaml.load(raw);
}

function collectGroups(inventory) {
  const children = inventory.all?.children ?? {};
  const groups = [];
  for (const [name, body] of Object.entries(children)) {
    if (!body || typeof body !== 'object') continue;
    if (!body.hosts) continue;
    const hosts = Object.entries(body.hosts).map(([hostname]) => ({
      hostname,
    }));
    if (hosts.length === 0) continue;
    groups.push({
      id: name,
      label: ROLE_LABELS[name] ?? name,
      description: GROUP_DESCRIPTIONS[name] ?? '',
      hosts: hosts.sort((a, b) => a.hostname.localeCompare(b.hostname)),
    });
  }
  return groups.sort((a, b) => a.id.localeCompare(b.id));
}

function render(groups) {
  const header = `// AUTO-GENERATED by scripts/build-lab-content.mjs — do not edit by hand.
// Regenerate: \`npm run build:lab\`
// Source:     ansible/inventory/hosts.yml
// Generated:  ${new Date().toISOString()}

export interface LabHost {
  readonly hostname: string;
}

export interface LabHostGroup {
  readonly id: string;
  readonly label: string;
  readonly description: string;
  readonly hosts: readonly LabHost[];
}

export const LAB_HOST_GROUPS: readonly LabHostGroup[] = ${JSON.stringify(groups, null, 2)} as const;

export const LAB_HOST_COUNT = ${groups.reduce((n, g) => n + g.hosts.length, 0)};
`;
  return header;
}

function main() {
  const inventory = loadInventory();
  const groups = collectGroups(inventory);
  mkdirSync(dirname(outPath), { recursive: true });
  writeFileSync(outPath, render(groups), 'utf8');
  console.log(`lab.generated.ts ← ${groups.length} groups / ${groups.reduce((n, g) => n + g.hosts.length, 0)} hosts`);
}

main();
